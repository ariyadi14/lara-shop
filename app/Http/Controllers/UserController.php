    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $model = new User;
        $model->name = $request->get('name');
        $model->username = $request->get('username');
        $model->roles = json_encode($request->get('roles'));
        $model->phone = $request->get('phone');
        $model->address = $request->get('address');
        $model->email = $request->get('email');
        $model->password = \Hash::make($request->get('password'));
        if ($request->file('avatar')) {
            $file = $request->file('avatar')->store('avatar', 'public');
            $model->avatar = $file;
        }

        $model->save();

        return redirect()->route('users.create')->with('status', 'User Successfully Create');
    }

